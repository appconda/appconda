    public static fromPhoneNumber(number: string): string | null {
        const digits = number.replace(/[+\s()-]/g, '');

        // Remove international call prefix, usually `00` or `011`
        const cleanedDigits = digits.replace(/^00|^011/, '');

        // Prefixes can be 3, 2, or 1 digits long
        // Attempt to match the longest first
        for (const length of [3, 2, 1]) {
            const code = cleanedDigits.substring(0, length);
            if (CallingCode.CODES[code]) {
                return code;
            }
        }

        return null;
    }
